# Luas = 1/2 x alas x tinggi

# meminta inputan Luas
alas = float(input('Masukan value alas: '))  
tinggi = float(input('Masukan value tinggi: '))  

# menghitung luas segitiga  
luas = (alas * tinggi) / 2  
print("luasnya adalah: ", luas)


